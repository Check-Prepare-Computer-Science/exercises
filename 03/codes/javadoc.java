/**
 * This method accepts two real numbers that belong to a
 * vector and calculates the euclidean norm of the said
 * vector.
 *
 * @param x the first component of a two-dimensional vector (x, y)
 * @param y the second component of a two-dimensional vector (x, y)
 * @return the euclidean norm of the vector (x, y)
 */
 double euclid2(float x, float y) {
	 return Math.sqrt(x*x + y*y);
 }